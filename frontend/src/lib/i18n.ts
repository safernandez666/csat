export type Language = "en" | "es" | "pt";

export const SUPPORTED_LANGUAGES: { value: Language; label: string }[] = [
  { value: "en", label: "English" },
  { value: "es", label: "Español" },
  { value: "pt", label: "Português" },
];

export const LANGUAGE_STORAGE_KEY = "csat:lang";
export const LANGUAGE_CHANGE_EVENT = "csat:lang-change";

export function getStoredLanguage(): Language {
  if (typeof window === "undefined") return "en";
  const raw = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (raw === "en" || raw === "es" || raw === "pt") return raw;
  return "en";
}

export function setStoredLanguage(lang: Language): void {
  if (typeof window === "undefined") return;
  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, lang);
  document.documentElement.lang = lang;
  window.dispatchEvent(new CustomEvent(LANGUAGE_CHANGE_EVENT, { detail: lang }));
}

